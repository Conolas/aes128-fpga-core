`timescale 1ns/1ps

module AES_core_tb;

    // ============================================
    // INPUTS
    // ============================================

    reg clock;
    reg reset;
    reg start;

    reg [127:0] plain_text;
    reg [127:0] key;

    // ============================================
    // OUTPUTS
    // ============================================

    wire [127:0] cipher_text;
    wire done;

    // ============================================
    // DUT
    // ============================================

    AES_core DUT(

        .clock(clock),
        .reset(reset),
        .start(start),

        .plain_text(plain_text),
        .key(key),

        .cipher_text(cipher_text),
        .done(done)

    );

    // ============================================
    // CLOCK GENERATION
    // ============================================

    initial
    begin
        clock = 0;
        forever #5 clock = ~clock;
    end

    // ============================================
    // TEST
    // ============================================

    initial
    begin

        // ========================================
        // INITIAL VALUES
        // ========================================

        reset = 0;
        start = 0;

        plain_text =
128'h48454C4C4F20574F524C440000000000;

        key =
        128'h000102030405060708090A0B0C0D0E0F;

        // ========================================
        // RESET
        // ========================================

        #20;
        reset = 1;

        // ========================================
        // START ENCRYPTION
        // ========================================

        #20;
        start = 1;

        #10;
        start = 0;

        // ========================================
        // WAIT
        // ========================================

        wait(done);

        // ========================================
        // DISPLAY RESULTS
        // ========================================

        $display("\n=================================");
        $display("PLAINTEXT  = %h", plain_text);
        $display("KEY        = %h", key);
        $display("CIPHERTEXT = %h", cipher_text);
        $display("=================================\n");

        #50;

        $stop;

    end

endmodule